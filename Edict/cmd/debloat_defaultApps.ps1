$appxprovisionedpackage = Get-AppxProvisionedPackage -Online

foreach ($app in $apps) {
    Write-Output "Trying to remove $app"

    try {
        $packages = Get-AppxPackage -Name $app -AllUsers -ErrorAction SilentlyContinue
        if ($packages) {
            $packages | Remove-AppxPackage -AllUsers -ErrorAction SilentlyContinue
        }
    } catch {
        Write-Warning "Failed to remove AppxPackage $app : $_"
    }

    try {
        ($appxprovisionedpackage).Where( {$_.DisplayName -EQ $app}) |
            Remove-AppxProvisionedPackage -Online -ErrorAction SilentlyContinue
    } catch {
        Write-Warning "Failed to remove AppxProvisionedPackage $app : $_"
    }
}

